#!/usr/bin/env node
/**
 * Generates supabase/functions/_shared/questionPacks.ts from the client-side
 * pack JSON files.
 *
 * IMPORTANT: only question IDs are emitted to the server module — never the
 * question text. Question text lives exclusively in the static client-side
 * files (src/data/packs/*.json) and is resolved on the client. The server and
 * Realtime layer deal only in pack IDs and question IDs, so no mature (or any)
 * question text is ever bundled into Edge Functions, stored, or broadcast.
 *
 * Run after editing any file in src/data/packs/:
 *   node scripts/generate-edge-packs.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const ids = (name) =>
  JSON.parse(readFileSync(join(root, 'src/data/packs', `${name}.json`), 'utf8')).map((q) => q.id);

const boys = ids('boys');
const girls = ids('girls');
const infamous = ids('infamous');

const out = `// AUTO-GENERATED by scripts/generate-edge-packs.mjs — do not edit by hand.
// Source of truth: src/data/packs/*.json. Regenerate after any pack change.
//
// Only question IDs live here — never question text. Text stays in the static
// client files and is resolved client-side. The server/Realtime layer transmits
// and stores only pack IDs and question IDs.

export type PackId = 'boys' | 'girls' | 'infamous'

export function isValidPack(v: unknown): v is PackId {
  return v === 'boys' || v === 'girls' || v === 'infamous'
}

const BANK_IDS: Record<PackId, number[]> = {
  boys: ${JSON.stringify(boys)},
  girls: ${JSON.stringify(girls)},
  infamous: ${JSON.stringify(infamous)},
}

export function packIds(pack: PackId): number[] {
  return BANK_IDS[pack] ?? []
}

/** Draw one question id from a pack, avoiding ids already used this session. */
export function pickQuestion(pack: PackId, usedIds: number[]): number {
  const bank = packIds(pack)
  const available = bank.filter((id) => !usedIds.includes(id))
  const pool = available.length > 0 ? available : bank
  return pool[Math.floor(Math.random() * pool.length)]
}

/** Draw \`count\` decoy ids from the same pack, excluding the correct id. */
export function pickDecoys(pack: PackId, correctId: number, usedIds: number[], count: number): number[] {
  const pool = packIds(pack).filter((id) => id !== correctId && !usedIds.includes(id))
  return [...pool].sort(() => Math.random() - 0.5).slice(0, count)
}
`;

const dest = join(root, 'supabase/functions/_shared/questionPacks.ts');
writeFileSync(dest, out);
console.log(
  `Wrote ${dest} (IDs only)\n  boys: ${boys.length}  girls: ${girls.length}  infamous: ${infamous.length}`
);
